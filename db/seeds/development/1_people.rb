require Rails.root.join('db', 'seeds', 'support', 'person_seeder')

class Wsjrdp2023PersonSeeder < PersonSeeder

  def amount(role_type)
    case role_type.name.demodulize
    when 'Member' then 5
    else 1
    end
  end

end

